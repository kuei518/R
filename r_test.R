#直接輸入資料
c(1,2,3,4,5)
#可以給他取個名字為x
c(1,2,3,4,5) -> x
x

#假如不是數字而是文字(例如abcde),要用引號括起來
c("a","b","c","d","e")
#也可以取個名字為x1
c("a","b","c","d","e") -> x1
x1

#把x跟x1排在一起,有兩種排法
#第一種:前接尾,用c
c(x,x1)
#第二種:橫著排,用rbind(row bind的意思)
rbind(x,x1)
#第二種:縱著排,cbind
cbind(x,x1)

#靠scan這個函數來輸入,會啟動一個互動式的輸入介面,例如輸入23456後,他會讀到23456;再給他一個名字x2
scan()
2
3
4
5
6
scan() -> x2
x2
